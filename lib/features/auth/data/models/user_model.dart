import 'package:json_annotation/json_annotation.dart';
import '../../domain/entities/user_entity.dart';

part 'user_model.g.dart';

@JsonSerializable()
class UserModel extends User {
  const UserModel({
    required super.id,
    String? lastname,
    String? firstname,
    String? nickname,
    String? gender,
    String? nationality,
    String? birthplace,
    String? address,
    String? dateOfBirth,
    String? photo,
    String? skype,
    String? gmail,
    String? discord,
    String? whatsapp,
    String? facebook,
    String? twitter,
    String? instagram,
    String? linkedin,
    String? phoneWork,
    String? profession,
    super.updatedAt,
    required super.username,
    required String tel,
    String? email,
    required bool emailVerified,
    required String dateJoined,
    required String lastLogin,
    String? lastIp,
    String? lastLocation,
    String? imei,
    required bool isVerified,
    required bool isStaff,
    required bool isActive,
    required bool isSuperuser,
    super.userQuater,
    List<dynamic>? groups,
    List<dynamic>? userPermissions,
    List<dynamic>? competence,
    List<dynamic>? job,
    List<dynamic>? status,
  }) : super(
          lastname: lastname,
          firstname: firstname,
          nickname: nickname,
          gender: gender,
          nationality: nationality,
          birthplace: birthplace,
          address: address,
          dateOfBirth: dateOfBirth,
          photo: photo,
          skype: skype,
          gmail: gmail,
          discord: discord,
          whatsapp: whatsapp,
          facebook: facebook,
          twitter: twitter,
          instagram: instagram,
          linkedin: linkedin,
          phoneWork: phoneWork,
          profession: profession,
          tel: tel,
          email: email,
          emailVerified: emailVerified,
          dateJoined: dateJoined,
          lastLogin: lastLogin,
          lastIp: lastIp,
          lastLocation: lastLocation,
          imei: imei,
          isVerified: isVerified,
          isStaff: isStaff,
          isActive: isActive,
          isSuperuser: isSuperuser,
          groups: groups ?? const [],
          userPermissions: userPermissions ?? const [],
          competence: competence ?? const [],
          job: job ?? const [],
          status: status ?? const [],
        );

  factory UserModel.fromJson(Map<String, dynamic> json) => _$UserModelFromJson(json);
  Map<String, dynamic> toJson() => _$UserModelToJson(this);
}
